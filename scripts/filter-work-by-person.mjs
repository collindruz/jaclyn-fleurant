/**
 * COCO-SSD person detection on work images. Does not create or modify image files;
 * only writes filename lists and a debug log.
 *
 * Input:  public/images/work/site-pull
 * Output: lib/work-person-filtered.ts  (PERSON_IMAGE_FILENAMES)
 *         lib/work-person-filtered.json (same list for scripts)
 * Log:    public/debug/work-person-filter.json
 *
 * Run: npm run work:filter:person
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { createRequire } from "module";
import sharp from "sharp";

const require = createRequire(import.meta.url);
const tf = require("@tensorflow/tfjs");
const cocossd = require("@tensorflow-models/coco-ssd");

function rgbBufferToTensor3d(data, w, h, channels) {
  const src = data instanceof Uint8Array ? data : new Uint8Array(data);
  const out = new Uint8Array(w * h * 3);
  for (let i = 0; i < w * h; i++) {
    const s = i * channels;
    const d = i * 3;
    out[d] = src[s];
    out[d + 1] = src[s + 1];
    out[d + 2] = src[s + 2];
  }
  return tf.tensor3d(out, [h, w, 3]);
}

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const IN_DIR = path.join(ROOT, "public", "images", "work", "site-pull");
const OUT_TS = path.join(ROOT, "lib", "work-person-filtered.ts");
const OUT_JSON = path.join(ROOT, "lib", "work-person-filtered.json");
const LOG_PATH = path.join(ROOT, "public", "debug", "work-person-filter.json");

const IMG_EXT = new Set([".jpg", ".jpeg", ".png", ".webp", ".gif", ".avif"]);

const DETECT_MAX_WIDTH = 800;
const PERSON_MIN_SCORE = 0.32;

/**
 * @param {Array<{ class: string; score: number; bbox: [number, number, number, number] }>} pred
 * @param {number} minScore
 */
function pickLargestPerson(pred, minScore) {
  const people = pred.filter(
    (p) => p.class === "person" && p.score >= minScore
  );
  if (!people.length) return null;
  let best = people[0];
  let a = best.bbox[2] * best.bbox[3];
  for (let i = 1; i < people.length; i++) {
    const p = people[i];
    const ar = p.bbox[2] * p.bbox[3];
    if (ar > a) {
      best = p;
      a = ar;
    }
  }
  return best;
}

/**
 * @param {unknown} personModel
 * @param {string} inPath
 * @returns {Promise<boolean>} true if a person is detected at or above threshold
 */
async function hasPersonInImage(personModel, inPath) {
  let imageTensor = null;
  try {
    const { data: detData, info: dInfo } = await sharp(inPath)
      .rotate()
      .resize({
        width: DETECT_MAX_WIDTH,
        withoutEnlargement: true,
        fit: "inside",
      })
      .ensureAlpha()
      .flatten({ background: { r: 255, g: 255, b: 255 } })
      .raw()
      .toBuffer({ resolveWithObject: true });

    const dW2 = dInfo.width || 1;
    const dH2 = dInfo.height || 1;
    const dCh = dInfo.channels || 3;
    imageTensor = rgbBufferToTensor3d(detData, dW2, dH2, dCh);
    const pred = await personModel.detect(imageTensor, 25, 0.2);
    return pickLargestPerson(pred, PERSON_MIN_SCORE) != null;
  } finally {
    if (imageTensor) {
      try {
        imageTensor.dispose();
      } catch {
        // ignore
      }
    }
  }
}

function relFromRoot(absPath) {
  return path.relative(ROOT, absPath).split(path.sep).join("/");
}

function isImageFile(e) {
  if (!e.isFile()) return false;
  return IMG_EXT.has(path.extname(e.name).toLowerCase());
}

/**
 * @param {string[]} includedList
 * @param {object} meta
 * @param {{ personFilterSuccess?: boolean }} [opt] personFilterSuccess true when detection ran; false = model failed (do not treat as applied filter for Work pipeline)
 */
function writeArtifactFiles(includedList, meta, opt = {}) {
  const personFilterSuccess = opt.personFilterSuccess !== false;
  const sorted = [...includedList].sort();
  const body = sorted.map((n) => `  ${JSON.stringify(n)},`).join("\n");
  const listInner = sorted.length ? `\n${body}\n` : "";
  const applied = personFilterSuccess;

  const ts = `/**
 * Auto-generated by \`npm run work:filter:person\` — do not edit by hand.
 * Full-frame files in site-pull where COCO-SSD found a person (not cropped here).
 * ${new Date().toISOString()}
 */
export const PERSON_FILTER_APPLIED = ${applied ? "true" : "false"} as const;
export const PERSON_IMAGE_FILENAMES = [${listInner}] as const;
`;

  fs.writeFileSync(OUT_TS, ts, "utf8");
  if (personFilterSuccess) {
    fs.writeFileSync(OUT_JSON, JSON.stringify({ filenames: sorted }, null, 0), "utf8");
  } else if (fs.existsSync(OUT_JSON)) {
    try {
      fs.unlinkSync(OUT_JSON);
    } catch {
      // ignore
    }
  }
  fs.writeFileSync(LOG_PATH, JSON.stringify(meta, null, 2), "utf8");
}

async function main() {
  if (!fs.existsSync(IN_DIR)) {
    console.warn(`[work:filter:person] Missing input: ${IN_DIR}`);
    fs.mkdirSync(path.dirname(LOG_PATH), { recursive: true });
    const meta = {
      generatedAt: new Date().toISOString(),
      inputDir: relFromRoot(IN_DIR),
      modelLoaded: false,
      detectMaxWidth: DETECT_MAX_WIDTH,
      personMinScore: PERSON_MIN_SCORE,
      summary: { total: 0, included: 0, excluded: 0, error: "missing-input-dir" },
      files: [],
    };
    const stub = `/**
 * Run npm run work:filter:person when site-pull exists. PERSON_FILTER_APPLIED becomes true
 * when that script completes successfully; color:group can use all of site-pull until then.
 */
export const PERSON_FILTER_APPLIED = false as const;
export const PERSON_IMAGE_FILENAMES = [] as const;
`;
    fs.writeFileSync(OUT_TS, stub, "utf8");
    if (fs.existsSync(OUT_JSON)) {
      try {
        fs.unlinkSync(OUT_JSON);
      } catch {
        // ignore
      }
    }
    fs.writeFileSync(LOG_PATH, JSON.stringify(meta, null, 2), "utf8");
    return;
  }

  const names = fs
    .readdirSync(IN_DIR, { withFileTypes: true })
    .filter(isImageFile)
    .map((d) => d.name)
    .sort();

  fs.mkdirSync(path.dirname(LOG_PATH), { recursive: true });

  /** @type {{ filename: string; included: boolean; reason: "person-detected" | "no-person" }[]} */
  const fileRows = [];
  const included = [];

  let personModel = null;
  try {
    await tf.setBackend("cpu");
    await tf.ready();
    personModel = await cocossd.load();
    console.log("[work:filter:person] COCO-SSD ready (tfjs CPU).");
  } catch (e) {
    const msg = e?.message || String(e);
    console.warn(
      `[work:filter:person] Model load failed. ${msg}`
    );
    for (const name of names) {
      fileRows.push({ filename: name, included: false, reason: "no-person" });
    }
    const meta = {
      generatedAt: new Date().toISOString(),
      inputDir: relFromRoot(IN_DIR),
      outTs: relFromRoot(OUT_TS),
      outJson: relFromRoot(OUT_JSON),
      modelLoaded: false,
      detectMaxWidth: DETECT_MAX_WIDTH,
      personMinScore: PERSON_MIN_SCORE,
      modelError: msg,
      summary: {
        total: names.length,
        included: 0,
        excluded: names.length,
      },
      files: fileRows,
    };
    writeArtifactFiles([], meta, { personFilterSuccess: false });
    console.log(
      `[work:filter:person] Wrote ${relFromRoot(OUT_TS)} and ${relFromRoot(LOG_PATH)} (0 included; model load failed; color:group will use all site-pull files).`
    );
    return;
  }

  for (let i = 0; i < names.length; i++) {
    const name = names[i];
    if (i % 30 === 0) {
      console.log(`[work:filter:person] ${i + 1}/${names.length}…`);
    }
    const inPath = path.join(IN_DIR, name);
    let ok = false;
    try {
      await sharp(inPath).rotate().metadata();
    } catch {
      fileRows.push({ filename: name, included: false, reason: "no-person" });
      continue;
    }
    try {
      ok = await hasPersonInImage(personModel, inPath);
    } catch {
      fileRows.push({ filename: name, included: false, reason: "no-person" });
      continue;
    }
    if (ok) {
      included.push(name);
      fileRows.push({ filename: name, included: true, reason: "person-detected" });
    } else {
      fileRows.push({ filename: name, included: false, reason: "no-person" });
    }
  }

  if (personModel && typeof personModel.dispose === "function") {
    try {
      personModel.dispose();
    } catch {
      // ignore
    }
  }

  const incN = included.length;
  const meta = {
    generatedAt: new Date().toISOString(),
    inputDir: relFromRoot(IN_DIR),
    outTs: relFromRoot(OUT_TS),
    outJson: relFromRoot(OUT_JSON),
    modelLoaded: true,
    detectMaxWidth: DETECT_MAX_WIDTH,
    personMinScore: PERSON_MIN_SCORE,
    summary: {
      total: names.length,
      included: incN,
      excluded: names.length - incN,
    },
    files: fileRows,
  };
  writeArtifactFiles(included, meta);
  console.log(
    `[work:filter:person] ${incN} included, ${names.length - incN} excluded. ` +
      `TS/JSON/ ${relFromRoot(LOG_PATH)}`
  );
}

main().catch((e) => {
  console.error("[work:filter:person] Fatal:", e);
  process.exit(1);
});
